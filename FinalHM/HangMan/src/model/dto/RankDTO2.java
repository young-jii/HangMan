package model.dto;

public class RankDTO2 {
	
	protected int allScore;
	protected int rankNo;
	protected int scoredId;
	protected int userId;
	
	public int getAllScore() {
		return allScore;
	}
	public void setAllScore(int allScore) {
		this.allScore = allScore;
	}
	public int getRankNo() {
		return rankNo;
	}
	public void setRankNo(int rankNo) {
		this.rankNo = rankNo;
	}
	public int getScoredId() {
		return scoredId;
	}
	public void setScoredId(int scoredId) {
		this.scoredId = scoredId;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
}
